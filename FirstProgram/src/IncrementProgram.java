public class IncrementProgram {

	public static void main(String[] args) {
		int numA=10;
		int numB=50;
		int numC=5;
	    //sum = --numA + numB--;
		System.out.println(numA);
		System.out.println(numB);
		System.out.println(numC);
	}

}

//POSTINCREMENT  numB=50 => numB++ =>51
//PREINCREMENT  numB=50 => ++numB =>51

//POSTDECREMENT  numB=50 => numB-- =>49
//PREDECREMENT  numB=50 => --numB =>49